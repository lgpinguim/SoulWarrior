void AMainCharacter::Attack()
{
	if (!bAttacking)
	{
		bAttacking = true;

		UAnimInstance* AnimInstance = GetMesh()->GetAnimInstance();
		if (AnimInstance && CombatMontage)
		{
			int32 Section = FMath::RandRange(0,3);
			if (ComboStage>4)
			{
				ComboStage=1;
			}
			switch (ComboStage)
			{
			case 1:
				AnimInstance->Montage_Play(CombatMontage, 0.75f);
				AnimInstance->Montage_JumpToSection(FName("Attack_1"), CombatMontage);
				break;
			case 2:
				AnimInstance->Montage_Play(CombatMontage, 0.75f);
				AnimInstance->Montage_JumpToSection(FName("Attack_2"), CombatMontage);
				break;
			case 3:
				AnimInstance->Montage_Play(CombatMontage, 0.75f);
				AnimInstance->Montage_JumpToSection(FName("Attack_3"), CombatMontage);
				break;
			case 4:
				AnimInstance->Montage_Play(CombatMontage, 0.75f);
				AnimInstance->Montage_JumpToSection(FName("Attack_4"), CombatMontage);
				break;
			default:
				;
			}
			ComboStage++;
		}
	}
}

void AMainCharacter::AttackEnd()
{
	bAttacking = false;
	if (bLMBDown)
	{
		Attack();
	}
}
